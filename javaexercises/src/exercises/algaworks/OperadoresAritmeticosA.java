package exercises.algaworks;

public class OperadoresAritmeticosA {
    public static void main(String[] args) {
        System.out.println("===============================");

        double x = 7;
        double y = 3;

        System.out.println("7 + 3: " + (x + y));


        System.out.println("7 - 3: "
                + (x - y));

        System.out.println("7 * 3: "
                + (x * y));

        System.out.println("7 / 3: " + (x / y));

        System.out.println("7 % 3: "
                + (x % y));

        System.out.println("Fim!");
        System.out.println("===============================");
    }
}


